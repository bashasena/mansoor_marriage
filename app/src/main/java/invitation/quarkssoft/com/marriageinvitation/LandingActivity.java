    @Override
    public void onMapReady(GoogleMap googleMap) {
        gmap = googleMap;
        gmap.setMinZoomPreference(12);
        gmap.setIndoorEnabled(true);
        UiSettings uiSettings = gmap.getUiSettings();
        uiSettings.setIndoorLevelPickerEnabled(true);
        uiSettings.setMyLocationButtonEnabled(true);
        uiSettings.setMapToolbarEnabled(true);
        uiSettings.setCompassEnabled(true);
        uiSettings.setZoomControlsEnabled(true);

        String destLat="11.127583";//marriage hall
        String destLong="79.089520";
        LatLng ny = new LatLng(11.127583, 79.089520);

        MarkerOptions markerOptions = new MarkerOptions();
        markerOptions.position(ny);

        gmap.addMarker(markerOptions);

        gmap.moveCamera(CameraUpdateFactory.newLatLng(ny));
    }

    @Override
    protected void onActivityResult(int requestCode, int resultCode, @Nullable Intent data) {
        super.onActivityResult(requestCode, resultCode, data);

        String destLat="11.127583";//marriage hall
        String destLong="79.089520";

        Double longitude=11.127583;//current loacation
        Double latitude =79.089520;
        if(requestCode == 1){
            if(data!= null && data.getExtras() != null){
                Bundle extras = data.getExtras();

                 longitude = extras.getDouble("Longitude");
                 latitude = extras.getDouble("Latitude");
            }

            String uri = "http://maps.google.com/maps?saddr=" + latitude + "," + longitude + "&daddr=" + destLat + "," + destLong;
            Intent intent = new Intent(Intent.ACTION_VIEW, Uri.parse(uri));
            startActivity(intent);
        }


    }

